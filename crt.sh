crt.sh which is a web interface to a distributed database called the certificate transparency logs. It helps domain owners to monitor certificate issuance for their domain, and use that to detect mis-issued certificates.

Things that you can monitor using this are:
> certificate expiry of your certicates at one place.
> mis-issued domain certificate and then taking proper action on it.

In simple words crt.sh will help you to find all the SSL or TLS certificates of the particular domain

What is Certificate Transparency ?
Certificate Transparency is an open framework for monitoring SSL Certificates. It is created for Domain Owners to monitor certificate issuance for their domain, and use that to detect misissued certificates.
